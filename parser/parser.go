package parser

import (
	"tomprueba/ast"
	"tomprueba/lexer"
	"tomprueba/token"
)

type Parser struct {
	l *lexer.Lexer

	curToken  token.Token
	peekToken token.Token
}

func New(l *lexer.Lexer) *Parser {
	p := &Parser{l: l}

	//Lee dos tokens, se define curToken (current token) y peekToken (el siguiente token)
	p.nextToken()
	p.nextToken()

	return p
}

func (p *Parser) nextToken() {
	p.curToken = p.peekToken
	p.peekToken = p.l.NextToken()
}

